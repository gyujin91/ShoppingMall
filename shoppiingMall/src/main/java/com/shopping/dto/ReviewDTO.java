package com.shopping.dto;

import java.util.Date;

public class ReviewDTO {
    private int rno;				// 리뷰 번호(PK)
    private String review_title;	// 리뷰 제목
    private String review_content;	// 리뷰 내용
    private int prod_no;			// 상품 고유 번호(외래키)
    private String prod_name;		// 상품 이름
	private String prod_image;		// 상품 이미지
    private String mem_id;			// 리뷰 작성 회원(일반 유저)
    private String mem_name;		// 리뷰 작성자 이름(일반 유저)
    private String useyn;			// 사용 여부
    private Date reg_date;			// 리뷰 작성일
    private Date del_date;			// 리뷰 삭제일
	public int getRno() {
		return rno;
	}
	public void setRno(int rno) {
		this.rno = rno;
	}
	public String getReview_title() {
		return review_title;
	}
	public void setReview_title(String review_title) {
		this.review_title = review_title;
	}
	public String getReview_content() {
		return review_content;
	}
	public void setReview_content(String review_content) {
		this.review_content = review_content;
	}
	public int getProd_no() {
		return prod_no;
	}
	public void setProd_no(int prod_no) {
		this.prod_no = prod_no;
	}
	public String getProd_name() {
		return prod_name;
	}
	public void setProd_name(String prod_name) {
		this.prod_name = prod_name;
	}
	public String getProd_image() {
		return prod_image;
	}
	public void setProd_image(String prod_image) {
		this.prod_image = prod_image;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getMem_name() {
		return mem_name;
	}
	public void setMem_name(String mem_name) {
		this.mem_name = mem_name;
	}
	public String getUseyn() {
		return useyn;
	}
	public void setUseyn(String useyn) {
		this.useyn = useyn;
	}
	public Date getReg_date() {
		return reg_date;
	}
	public void setReg_date(Date reg_date) {
		this.reg_date = reg_date;
	}
	public Date getDel_date() {
		return del_date;
	}
	public void setDel_date(Date del_date) {
		this.del_date = del_date;
	}
    
	

}
